%% Built-in Functions

x = 1; y = 1; n = 1;

%% Math Operators

abs(x)		% Absolute value
sqrt(x)		% Square root
nthroot(x,n)	% Nth root
sign(x)		% Sign (1 if pos., 0 if 0, -1 if neg)
rem(x,y)	% Remainder of x divided by y
exp(x)		% Natural exponential function
log(x)		% Natural logarithm
log10(x)	% Log base 10

%% Rounding

round(x) 	% Round to nearest integer
fix(x)		% Round toward zero
floor(x) 	% Round toward negative infinity
ceil(x)		% Round toward positive infinity

%% Discrete Math

factor(x)	% List factors
gcd(x,y)	% Greatest common divisor
lcm(x,y)	% Least common multiple
rats(x)		% Convert decimal to fraction
factorial(n)	% n!
primes(x)	% List primes at or below x
isprime(x)	% Decide if x is prime (1 if true, 0 if false)

%% Trig Functions

sin(x)		% Sine (input in radians)
sind(x)		% Sine (input in degrees)
asin(x)		% Sine inverse (output in radians)
asind(x)	% Sine inverse (output in degrees)
sinh(x)		% Hyperbolic sine
asinh(x)	% Inverse hyperbolic sine

cos(x)		% Cosine (input in radians)
cosd(x)		% Cosine (input in degrees)
acos(x)		% Cosine inverse (output in radians)
acosd(x)	% Cosine inverse (output in degrees)
cosh(x)		% Hyperbolic cosine
acosh(x)	% Inverse hyperbolic cosine

tan(x)		% Tangent (input in radians)
tand(x)		% Tangent (input in degrees)
atan(x)		% Tangent inverse (output in radians)
atand(x)	% Tangent inverse (output in degrees)
tanh(x)		% Hyperbolic tangent
atanh(x)	% Inverse hyperbolic tangent

sec(x)		% Secant (input in radians)
secd(x)		% Secant (input in degrees)
asec(x)		% Secant inverse (output in radians)
asecd(x)	% Secant inverse (output in degrees)
sech(x)		% Hyperbolic secant
asech(x)	% Inverse hyperbolic secant

csc(x)		% Cosecant (input in radians)
cscd(x)		% Cosecant (input in degrees)
acsc(x)		% Cosecant inverse (output in radians)
acscd(x)	% Cosecant inverse (output in degrees)
csch(x)		% Hyperbolic cosecant
acsch(x)	% Inverse hyperbolic cosecant

cot(x)		% Cotangent (input in radians)
cotd(x)		% Cotangent (input in degrees)
acot(x)		% Cotangent inverse (output in radians)
acotd(x)	% Cotangent inverse (output in degrees)
coth(x)		% Hyperbolic cotangent
acoth(x)	% Inverse hyperbolic cotangent

%% Language Help

help		% Open help -- or use help <topic>
doc		% Open documntation -- or use doc <topic>
